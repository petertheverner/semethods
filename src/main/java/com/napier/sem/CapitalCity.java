package com.napier.sem;

public class CapitalCity {

    //variables for Capital City's
    private String Capitalname;
    private int Capitalpopulation;


    //Setters
    public void setCapitalpopulation(int CapitalPopulation) { this.Capitalpopulation =  CapitalPopulation; }
    public void setCapitalName(String CapitalName) { this.Capitalname = CapitalName; }

    //Getters
    public int getCapitalpopulation() { return this.Capitalpopulation; }
    public String getCapitalName() { return this.Capitalname; }
}
