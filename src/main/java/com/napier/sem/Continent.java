package com.napier.sem;

public class Continent {

    //variables for world continents
    private int Continentpopulation;
    private String ContinentName;


    public void setContinentpopulation(int ContinentPopulation) { this.Continentpopulation = ContinentPopulation; }
    public void setContinentName(String ContinentName) { this.ContinentName = ContinentName; }

    //Getters
    public int getContinentpopulation() { return this.Continentpopulation; }
    public String getContinentName() { return this.ContinentName; }

}
